package com.example.slpt;

public class BusDriver {

    private String LocationName, bustype, roadnumber,vehicleNum,r1start,r1stop,r2start,r2stop,startdestination,stopdestination,status;

    public BusDriver() {
    }

    public String getLocationName() {
        return LocationName;
    }

    public String getBustype() {
        return bustype;
    }

    public String getRoadnumber() {
        return roadnumber;
    }

    public String getVehicleNum() {
        return vehicleNum;
    }

    public String getR1start() {
        return r1start;
    }

    public String getR1stop() {
        return r1stop;
    }

    public String getR2start() {
        return r2start;
    }

    public String getR2stop() {
        return r2stop;
    }

    public String getStartdestination() {
        return startdestination;
    }

    public String getStopdestination() {
        return stopdestination;
    }

    public String getStatus() {
        return status;
    }
}
